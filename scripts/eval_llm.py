def call_model(
    prompt: str,
    model_name: str,
    provider: str,
    temperature: float = 0.0,
    max_tokens: int = 5,
    llama_mode: str = "finetuned",
    llama_base_model: str = "meta-llama/Llama-3.1-8B-Instruct",
) -> str:

    provider = provider.lower().strip()

    if provider == "openai":
        return call_openai_model(
            prompt=prompt,
            model_name=model_name,
            temperature=temperature,
            max_tokens=max_tokens,
        )
    elif provider == "anthropic":
        return call_anthropic_model(
            prompt=prompt,
            model_name=model_name,
            temperature=temperature,
            max_tokens=max_tokens,
        )
    elif provider == "gemini":
        return call_gemini_model(
            prompt=prompt,
            model_name=model_name,
            temperature=temperature,
            max_tokens=max_tokens,
        )
    elif provider == "gemini_vertex":
        return call_gemini_vertex_model(
            prompt=prompt,
            model_name=model_name,
            temperature=temperature,
            max_tokens=max_tokens,
        )
    elif provider == "llama":
        return call_llama_model(
            prompt=prompt,
            model_name=model_name,
            temperature=temperature,
            max_tokens=max_tokens,
            llama_mode=llama_mode,
            llama_base_model=llama_base_model,
        )
    else:
        raise ValueError(f"Unsupported provider: {provider}")


def classify_one(
    essay_text: str,
    model_name: str,
    provider: str,
    temperature: float = 0.0,
    max_retries: int = 3,
    sleep_seconds: float = 1.0,
    llama_mode: str = "finetuned",
    llama_base_model: str = "meta-llama/Llama-3.1-8B-Instruct",
):
    provider = provider.lower().strip()

    if provider == "llama":
        prompt = LLAMA_PROMPT_TEMPLATE.format(essay=str(essay_text))
        effective_max_retries = 1
        max_tokens = 3
    else:
        prompt = PROMPT_TEMPLATE.format(essay=str(essay_text))
        effective_max_retries = max_retries
        max_tokens = 16

    last_raw = None
    had_api_error = False

    for attempt in range(1, effective_max_retries + 1):
        try:
            raw_response = call_model(
                prompt=prompt,
                model_name=model_name,
                provider=provider,
                temperature=temperature,
                max_tokens=max_tokens,
                llama_mode=llama_mode,
                llama_base_model=llama_base_model,
            )
            last_raw = raw_response
            pred = normalize_label(raw_response)

            if pred is not None:
                return pred, raw_response, had_api_error

        except Exception as e:
            had_api_error = True
            last_raw = f"[ERROR] {type(e).__name__}: {e}"

            # Claude-only smarter retry behavior
            if provider == "anthropic":
                if attempt < effective_max_retries and is_retryable_anthropic_error(e):
                    wait_s = anthropic_backoff_seconds(attempt, base_sleep=max(2.0, sleep_seconds))
                    print(f"[Anthropic retry {attempt}/{effective_max_retries}] {type(e).__name__}: sleeping {wait_s:.2f}s")
                    time.sleep(wait_s)
                    continue

            # All other cases: simple retry
            if attempt < effective_max_retries:
                time.sleep(sleep_seconds)
                continue

        # If no exception but parsing failed, retry
        if attempt < effective_max_retries:
            if provider == "anthropic":
                wait_s = anthropic_backoff_seconds(attempt, base_sleep=max(2.0, sleep_seconds))
                time.sleep(wait_s)
            else:
                time.sleep(sleep_seconds)

    return None, last_raw, had_api_error


def load_llama_model(model_name, llama_mode="finetuned", llama_base_model="meta-llama/Llama-3.1-8B-Instruct"):
    cache_key = (model_name, llama_mode, llama_base_model)
    if cache_key in LLAMA_MODEL_CACHE:
        return LLAMA_MODEL_CACHE[cache_key]

    tokenizer = AutoTokenizer.from_pretrained(llama_base_model)

    if tokenizer.pad_token is None:
        tokenizer.pad_token = tokenizer.eos_token

    model = AutoModelForCausalLM.from_pretrained(
        llama_base_model,
        device_map="auto",
        torch_dtype=torch.bfloat16 if torch.cuda.is_available() else torch.float32,
    )

    if llama_mode == "finetuned":
        model = PeftModel.from_pretrained(model, model_name)
    elif llama_mode == "zeroshot":
        pass
    else:
        raise ValueError(f"Unsupported llama_mode: {llama_mode}")

    model.eval()

    LLAMA_MODEL_CACHE[cache_key] = (tokenizer, model)
    return tokenizer, model


def call_llama_model(
    prompt: str,
    model_name: str,
    temperature: float = 0.0,
    max_tokens: int = 3,
    llama_mode: str = "finetuned",
    llama_base_model: str = "meta-llama/Llama-3.1-8B-Instruct",
) -> str:
    tokenizer, model = load_llama_model(
        model_name=model_name,
        llama_mode=llama_mode,
        llama_base_model=llama_base_model,
    )

    messages = [
        {
            "role": "system",
            "content": "You are a classifier. Reply with exactly one word: Human or AI."
        },
        {
            "role": "user",
            "content": prompt
        },
    ]

    inputs = tokenizer.apply_chat_template(
        messages,
        tokenize=True,
        add_generation_prompt=True,
        return_tensors="pt",
        return_dict=True,
    ).to(model.device)

    outputs = model.generate(
        input_ids=inputs["input_ids"],
        attention_mask=inputs["attention_mask"],
        max_new_tokens=max_tokens,
        do_sample=False,
        eos_token_id=tokenizer.eos_token_id,
        pad_token_id=tokenizer.eos_token_id,
    )

    input_len = inputs["input_ids"].shape[1]
    gen_ids = outputs[0][input_len:]
    generated = tokenizer.decode(gen_ids, skip_special_tokens=True).strip()

    return generated


def print_confusion_details(cm):
    print("\nConfusion matrix (rows=true, cols=pred) [labels: 0=Human, 1=AI]:")
    print(cm)

    if cm.shape == (2, 2):
        tn, fp, fn, tp = cm.ravel()
        print("\nPer-class breakdown:")
        print(f"Human (0): support={tn + fp}, correct={tn}, incorrect={fp}, recall={tn / (tn + fp + 1e-12):.4f}")
        print(f"AI    (1): support={fn + tp}, correct={tp}, incorrect={fn}, recall={tp / (fn + tp + 1e-12):.4f}")


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--model_name", type=str, required=True)
    parser.add_argument("--data_file", type=str, required=True)

    parser.add_argument(
        "--provider",
        type=str,
        required=True,
        choices=["openai", "anthropic", "gemini", "gemini_vertex", "llama"]
    )

    parser.add_argument("--save_predictions", type=str, default=None)
    parser.add_argument("--save_metrics", type=str, default=None)

    parser.add_argument("--temperature", type=float, default=0.0)
    parser.add_argument("--max_retries", type=int, default=3)
    parser.add_argument("--sleep_seconds", type=float, default=1.0)

    parser.add_argument("--debug_n", type=int, default=None)
    parser.add_argument("--seed", type=int, default=42)

    parser.add_argument(
    "--llama_mode",
    type=str,
    default="finetuned",
    choices=["zeroshot", "finetuned"],
    help="For provider=llama: evaluate base model only (zeroshot) or base+LoRA adapter (finetuned)."
    )

    parser.add_argument(
        "--llama_base_model",
        type=str,
        default="meta-llama/Llama-3.1-8B-Instruct",
        help="Base HF model used for llama evaluation."
    )

    args = parser.parse_args()

    random.seed(args.seed)

    df = pd.read_csv(args.data_file)

    required_cols = {"text", "label"}
    missing = required_cols - set(df.columns)
    if missing:
        raise ValueError(f"Missing required columns in {args.data_file}: {sorted(missing)}")

    if args.debug_n is not None:
        sample_n = min(args.debug_n, len(df))
        df = df.sample(n=sample_n, random_state=args.seed).reset_index(drop=True)
        print(f"DEBUG MODE: using {sample_n} examples")

    print(f"Model: {args.model_name}")
    print(f"Eval file: {args.data_file}")
    print(f"Eval size: {len(df)}")
    print(f"Temperature: {args.temperature}")
    print(f"Max retries: {args.max_retries}")
    print(f"Provider: {args.provider}")

    if args.provider == "gemini_vertex":
        print("Using Gemini Vertex endpoint:", args.model_name)

    preds = []
    raw_outputs = []
    parse_failed = 0
    api_error_count = 0

    parsed_true = []
    parsed_pred = []

    texts = df["text"].tolist()
    true_labels = df["label"].tolist()

    for i, text in enumerate(texts, start=1):
        pred, raw_response, had_api_error = classify_one(
            essay_text=text,
            model_name=args.model_name,
            provider=args.provider,
            temperature=args.temperature,
            max_retries=args.max_retries,
            sleep_seconds=args.sleep_seconds,
            llama_mode=args.llama_mode,
            llama_base_model=args.llama_base_model,
        )
        if had_api_error:
            api_error_count += 1  # if had_api_error

        raw_outputs.append(raw_response)

        if args.debug_n is not None:
            print(f"\n--- Example {i} ---")
            print("RAW RESPONSE:")
            print(repr(raw_response))
            print("PARSED PRED:", pred)
            print("TRUE LABEL:", true_labels[i - 1])

        if pred is None:
            parse_failed += 1
        else:
            parsed_true.append(true_labels[i - 1])
            parsed_pred.append(pred)

        preds.append(pred)

        if i % 25 == 0 or i == len(texts):
            print(f"Processed {i}/{len(texts)}")

    valid_idx = [i for i, p in enumerate(preds) if p is not None]
    scored_true = [true_labels[i] for i in valid_idx]
    scored_preds = [preds[i] for i in valid_idx]

    if len(scored_preds) > 0:
        acc = accuracy_score(scored_true, scored_preds)
        report = classification_report(
            scored_true,
            scored_preds,
            labels=[0, 1],
            target_names=["Human", "AI"],
            zero_division=0,
        )
        cm = confusion_matrix(scored_true, scored_preds, labels=[0, 1])
    else:
        acc = None
        report = "N/A (0 resolved predictions)"
        cm = confusion_matrix([], [], labels=[0, 1])

    true_counts = pd.Series(true_labels).value_counts().sort_index()
    pred_counts = pd.Series(scored_preds).value_counts().sort_index()
    unresolved_pred = len(preds) - len(scored_preds)

    human_true = int(true_counts.get(0, 0))
    ai_true = int(true_counts.get(1, 0))
    unresolved_pred = int(unresolved_pred)

    human_pred = int(pred_counts.get(0, 0))
    ai_pred = int(pred_counts.get(1, 0))

    majority_acc = max(human_true, ai_true) / len(true_labels) if len(true_labels) > 0 else float("nan")

    print("\nLabel distribution:")
    print(f"True counts: Human={human_true}, AI={ai_true}")
    print(f"Pred counts: Human={human_pred}, AI={ai_pred}, Unresolved={unresolved_pred}")
    print(f"\nMajority-class baseline accuracy: {majority_acc:.4f}")

    print_confusion_details(cm)

    if acc is not None:
        print(f"\nAccuracy: {acc:.4f}")
    else:
        print("\nAccuracy: N/A (0 resolved predictions)")

    print(report)
    print(f"\nParse failures: {parse_failed}/{len(df)}")
    print(f"Examples with API errors (during retries): {api_error_count}/{len(df)}")

    if len(parsed_true) > 0:
        parsed_acc = accuracy_score(parsed_true, parsed_pred)
        print(f"Accuracy on parsed outputs only: {parsed_acc:.4f} ({len(parsed_true)}/{len(df)} parsed)")
    else:
        parsed_acc = None
        print("Accuracy on parsed outputs only: N/A (0 parsed)")

    out_df = df.copy()
    out_df["pred"] = preds
    out_df["correct"] = out_df.apply(
        lambda row: int(row["pred"] == row["label"]) if pd.notna(row["pred"]) else pd.NA,
        axis=1,
    )
    out_df["raw_response"] = raw_outputs
    out_df["parse_failed"] = out_df["pred"].isna().astype(int)
    out_df["text_length"] = out_df["text"].astype(str).str.len()
    out_df["response_length"] = out_df["raw_response"].astype(str).str.len()
    out_df["api_error"] = out_df["raw_response"].astype(str).str.startswith("[ERROR]").astype(int)

    if parse_failed > 0:
        print("\nExamples of parse failures:")
        failed_rows = out_df[out_df["parse_failed"] == 1].head(5)
        for idx, row in failed_rows.iterrows():
            print(f"\nRow {idx}")
            print("Raw response:", repr(row["raw_response"]))

    if "source" in out_df.columns:
        print("\nAccuracy by source:")
        print(out_df.groupby("source")["correct"].mean().sort_values(ascending=False))

        print("\nCounts by source:")
        print(out_df["source"].value_counts())

    if args.save_predictions:
        if os.path.dirname(args.save_predictions):
            os.makedirs(os.path.dirname(args.save_predictions), exist_ok=True)
        out_df.to_csv(args.save_predictions, index=False)
        print(f"\nSaved predictions to: {args.save_predictions}")

    metrics = {
        "timestamp": datetime.now().isoformat(),
        "model_name": args.model_name,
        "provider": args.provider,
        "data_file": args.data_file,
        "eval_size": len(df),
        "temperature": args.temperature,
        "max_retries": args.max_retries,
        "sleep_seconds": args.sleep_seconds,
        "debug_n": args.debug_n,
        "seed": args.seed,
        "accuracy": acc,
        "parsed_only_accuracy": parsed_acc,
        "majority_class_baseline_accuracy": majority_acc,
        "parse_failures": parse_failed,
        "parse_failure_rate": parse_failed / len(df) if len(df) > 0 else None,
        "examples_with_api_error": api_error_count,
        "true_counts": {"Human": human_true, "AI": ai_true},
        "pred_counts": {"Human": human_pred, "AI": ai_pred, "Unresolved": unresolved_pred},
        "resolved_predictions": len(scored_preds),
        "confusion_matrix": cm.tolist(),
        "classification_report_text": report,
        "llama_mode": args.llama_mode if args.provider == "llama" else None,
        "llama_base_model": args.llama_base_model if args.provider == "llama" else None,
    }

    if args.save_metrics:
        if os.path.dirname(args.save_metrics):
            os.makedirs(os.path.dirname(args.save_metrics), exist_ok=True)
        save_json(metrics, args.save_metrics)
        print(f"Saved metrics to: {args.save_metrics}")


if __name__ == "__main__":
    main()