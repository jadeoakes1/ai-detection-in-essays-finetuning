def save_json(data, path):
    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, indent=2)


def normalize_label(raw_text: str):
    if raw_text is None:
        return None

    text = str(raw_text).strip()
    if not text:
        return None

    lowered = text.lower()

    # keep only first non-empty line
    lines = [line.strip() for line in lowered.splitlines() if line.strip()]
    first_line = lines[0] if lines else lowered

    # first token, stripped of punctuation
    first_token = re.split(r"\s+", first_line)[0].strip(" \t\n\r.,:;!?\"'()[]{}")

    if first_token == "human":
        return 0
    if first_token == "ai":
        return 1

    # allow simple prefixes like "Label: AI"
    early_text = lowered[:40]
    if re.search(r"\b(label|answer|prediction)\b\s*[:\-]?\s*human\b", early_text):
        return 0
    if re.search(r"\b(label|answer|prediction)\b\s*[:\-]?\s*ai\b", early_text):
        return 1

    # exact line fallback
    if re.fullmatch(r"human[.!]?", first_line, flags=re.IGNORECASE):
        return 0
    if re.fullmatch(r"ai[.!]?", first_line, flags=re.IGNORECASE):
        return 1

    # conservative whole-text fallback
    has_human = re.search(r"\bhuman\b", lowered) is not None
    has_ai = re.search(r"\bai\b", lowered) is not None

    if has_human and not has_ai:
        return 0
    if has_ai and not has_human:
        return 1

    return None


def call_openai_model(prompt: str, model_name: str, temperature: float = 0.0, max_tokens: int = 16) -> str:
    response = OPENAI_CLIENT.responses.create(
        model=model_name,
        input=prompt,
        temperature=temperature,
        max_output_tokens=max_tokens,
    )
    return response.output_text.strip()


def call_anthropic_model(prompt: str, model_name: str, temperature: float = 0.0, max_tokens: int = 16) -> str:
    response = ANTHROPIC_CLIENT.messages.create(
        model=model_name,
        max_tokens=max_tokens,
        temperature=temperature,
        messages=[
            {"role": "user", "content": prompt}
        ],
    )

    text_chunks = []
    for block in response.content:
        if getattr(block, "type", None) == "text":
            text_chunks.append(block.text)

    return "".join(text_chunks).strip()


def is_retryable_anthropic_error(exc: Exception) -> bool:
    msg = str(exc).lower()
    retryable_markers = [
        "overloaded",
        "529",
        "rate limit",
        "429",
        "timeout",
        "timed out",
        "connection",
        "apiconnectionerror",
        "internal server error",
        "502",
        "503",
        "504",
    ]
    return any(marker in msg for marker in retryable_markers)


def anthropic_backoff_seconds(attempt: int, base_sleep: float = 2.0) -> float:
    # Exponential backoff + small jitter
    return base_sleep * (2 ** (attempt - 1)) + random.uniform(0, 0.5)


def call_gemini_model(prompt: str, model_name: str, temperature: float = 0.0, max_tokens: int = 16) -> str:
    if GEMINI_CLIENT is None:
        raise ValueError("GEMINI_API_KEY not set but provider='gemini' was used")
    
    response = GEMINI_CLIENT.models.generate_content(
        model=model_name,
        contents=prompt,
        config={
            "temperature": temperature,
            "max_output_tokens": max_tokens,
            "thinking_config": {
                "thinking_budget": 0
            },
        },
    )

    text = getattr(response, "text", None)
    if text:
        text = text.strip()
        if text:
            return text

    candidates = getattr(response, "candidates", None)
    if candidates:
        for cand in candidates:
            content = getattr(cand, "content", None)
            parts = getattr(content, "parts", None) if content is not None else None
            if not parts:
                continue

            texts = []
            for p in parts:
                part_text = getattr(p, "text", None)
                if part_text:
                    texts.append(part_text)

            joined = "".join(texts).strip()
            if joined:
                return joined

    return f"[GEMINI EMPTY RESPONSE] {repr(response)}"


